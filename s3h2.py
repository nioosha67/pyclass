#tabei ba 3 voroodi, 2 adad va 1 amalgar, mohasebe 2 adad bar asase amalgar

def mohasebe(adad1,adad2,amalgar):
    if(amalgar=='+'):
        print(adad1+adad2)
    elif(amalgar=='-'):
        print(adad1-adad2)
    elif(amalgar=='*'):
        print(adad1*adad2)
    else:
        print(adad1/adad2)
mohasebe(100,2,'/')
